function navSection(siteName: string, pages: string[]): string {
  const links = pages
    .map((p) => `    <a href="${p === 'index' ? '/' : `/${p}`}">${capitalize(p)}</a>`)
    .join('\n');
  return `<nav class="site-nav">
  <div class="container">
    <a href="/" class="nav-brand">${siteName}</a>
    <div class="nav-links">
${links}
    </div>
  </div>
</nav>`;
}

