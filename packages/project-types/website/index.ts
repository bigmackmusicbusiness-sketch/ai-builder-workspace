function buildMetadataJson(siteName: string, slug: string, pages: string[]): string {
  return JSON.stringify(
    {
      siteName,
      slug,
      pages: pages.map((p) => ({
        slug: p,
        title: p === 'index' ? siteName : `${capitalize(p)} | ${siteName}`,
        description: `${capitalize(p === 'index' ? siteName : p)} page.`,
        og: { title: p === 'index' ? siteName : `${capitalize(p)} | ${siteName}`, type: 'website' },
        canonical: p === 'index' ? '/' : `/${p}`,
        schemaOrg: { '@context': 'https://schema.org', '@type': 'WebPage', name: siteName },
      })),
    },
    null,
    2,
  );
}

function buildWranglerToml(slug: string): string {
  return `# Cloudflare Pages publish config
name = "${slug}"
compatibility_date = "2024-01-01"
pages_build_output_dir = "."
`;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1).replace(/-/g, ' ');
}

// ---------------------------------------------------------------------------
// Project type definition
// ---------------------------------------------------------------------------

export const website: ProjectType = {
  id:          'website',
  label:       'Website',
  description: 'Multi-page marketing site with SEO, sections, and Cloudflare Pages publishing.',
  icon:        '🌐',

  scaffold(input: ScaffoldInput): FileTree {
    const pages  = input.pages?.length ? input.pages : ['index', 'about', 'contact'];
    const accent = input.accentColor ?? '#6366f1';
    const tree: FileTree = {};

    // HTML pages
    for (const page of pages) {
      const filename = page === 'index' ? 'index.html' : `${page}.html`;
      tree[filename] = page === 'index'
        ? buildIndexPage(input.projectName, pages, accent)
        : buildGenericPage(input.projectName, page, pages);
    }

    // Shared stylesheet
    tree['styles.css'] = buildStyles(accent);

    // SEO metadata manifest
    tree['metadata.json'] = buildMetadataJson(input.projectName, input.projectSlug, pages);

    // Publish config
    tree['wrangler.toml'] = buildWranglerToml(input.projectSlug);

    // README
    tree['README.md'] = `# ${input.projectName}\n\n${input.description ?? 'Website project.'}\n\n## Pages\n${pages.map((p) => `- \`${p === 'index' ? 'index.html' : p + '.html'}\``).join('\n')}\n\n## Publish\n\nDeploy to Cloudflare Pages via the Publish tab.\n`;

    return tree;
  },

  defaultVerificationMatrix: [
    'lint',
    'typecheck',
    'secretScan',
    'playwrightRuntime',
    'screenshotDiff',
  ],

  defaultApprovalPolicy: {
    alwaysApprove: ['publish.live'],
  },

  screens: ['preview', 'code', 'files', 'tests', 'visualqa', 'split'],
};
